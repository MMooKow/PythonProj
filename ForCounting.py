#Counting program with for loops and the range() func
print("Counting to 10...")
for i in range(11):
    print(i, end=" ")

print("\n\nCounting by 10s...")
for i in range(0,101,10):
    print(i, end=" ")

print("\n\nCounting backwards..")
for i in range(10, 0, -1):
      print(i, end=" ")

input("\n\nPress the enter key to continue")
